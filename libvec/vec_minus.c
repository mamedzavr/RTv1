#include "libvec.h"

t_vector3	vec_minus(t_vector3 a, t_vector3 b)
{
	t_vector3	r;

	r.x = a.x - b.x;
	r.y = a.y - b.y;
	r.z = a.z - b.z;
	return (r);
}
